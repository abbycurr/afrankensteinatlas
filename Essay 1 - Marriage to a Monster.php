<h2> Marriage to a Monster </h2>
<p> Women are consistently portrayed as flat and unemotional characters in Frankenstein, in direct contrast to both Victor Frankenstein's volitile emotional state and also in contrast to the prevailing theories at the time about female emotional instability and hysteria. These stable female characters also provide a contrast to Mary Shelley's own adventurous and arguably outlandish entrance into both adulthood and motherhood. This argue will explore these contrasts and argue that Mary Shelley created a world in which the female characters lived stable and consistent lives to compensate for her own wild upbringing. Specifically, we will examine the lives of Elizabeth, Safie and Margaret with a heavy focus on their marital status. </p>


